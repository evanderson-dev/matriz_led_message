int getFontIndex(char c) {
    if (c >= 'A' && c <= 'Z') return c - 'A';           // A-Z: 0-25
    if (c >= '0' && c <= '9') return 26 + (c - '0');   // 0-9: 26-35
    if (c == ' ') return 36;                            // Espaço: 36
    if (c == '<') return 37;                            // Coração: 37
    return 36;                                          // Default: espaço
}